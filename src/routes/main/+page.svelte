                Proof of Stake or Pay to Govern?
            </button>
            </h2>
            <div id="flush-collapse12" class="accordion-collapse collapse" aria-labelledby="flush-heading12" data-bs-parent="#accordionFlushExample">
              <div class="accordion-body">
                Holding an asset, assuming it has any exchange value, makes one a stakeholder. Proving that you hold the asset is therefore proof of stake. <br><br>
                An even better way to prove something at stake is to deposit and wrap x amount of X token in a governance structure that inflates its wrapper at a certain rate. <br><br>
                In a sense, you pay to be in that governance structure. You pay to participate and therefore, you pay to govern. <br><br>
                This alone is likely to generate some interesting dynamics as a whale splashing out of nowhere in the governance pool is a deflationary event and a good opportunity for others to exit at favourable terms. <br><br>
                At the same time there is an incentive to never enter the governance pool since that comes with a guaranteed loss versus simple token holding. <br><br>
                Changes in governance dynamics, power distributions will affect price.

