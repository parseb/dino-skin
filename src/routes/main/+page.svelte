Case 2: Street Corner Garden
<br><br>

There’s a plot of abandoned land at the end of the street. You’re a crypto developer. What do you do? 
<br><br>
You create an ERC20, you airdrop 1% on 900 leaflets. You set aside one to five leaflets for you and your …future friends. 
You set up a WalllaW DAO with $MyStreetGarden as base value. Immutably stream into the DAO 99% of the token over the next 5 years. Sent the base inflation to 10%. Create two subDAOs ‘material acquisitions’, ‘research and labour’. And I guess wait… or go to nearby community meetings and explain what you want to do. Because let’s be real, you do want to touch soli and grow food and like, speak with people in the ‘real’ world. <br><br> Likely, there’s others out there that not only want, but would be open to supporting such a socio-economic experiment. And this is what DAOs are for. Weird, previously unimaginable institutions, initiatives and generally, would argue, resilience.

