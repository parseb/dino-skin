            </h2>
            <div id="flush-collapse8" class="accordion-collapse collapse" aria-labelledby="flush-heading8" data-bs-parent="#accordionFlushExample">
              <div class="accordion-body">
                A membrane is a border. Something that separates or categorises. <br> <br>
                 In the context of WalllaW, membranes are a security device that determine whether an agent is or is not a member of a given DAO or subDAO. A binary determinator. <br>
                 They are comprised of a required list of Tokens and Balances as a membership condition. <br><br>
                 Only members can propose and support internal change such as inflation rate, membrane to use or the execution of externally oriented actions. <br><br>
                 Given that it can be changed, the membrane is capable of acting as an immune organ. Or as a modularization device.
            </div>
