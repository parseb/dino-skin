              <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapse9" aria-expanded="false" aria-controls="flush-collapse9">
                How It Moves?
              </button>
            </h2>
            <div id="flush-collapse9" class="accordion-collapse collapse" aria-labelledby="flush-heading9" data-bs-parent="#accordionFlushExample">
              <div class="accordion-body">
                There’s new movement, there’s movement in progress and then there’s inertia. The most efficient way to move is to make use of inertia to the widest extent possible. Inflation and interest rates are forms of inertia yielding global movement. Changes in inflation rate affects the speed, intensity and coherence of lower-level, localised or specialised agents. 
                <br> <br>
Another way <b> to move is to signal </b> out a desirable point and expend energy to get there. In WalllaW this will have to take the shape of a subDAO. Only members can spawn new subDAOs of DAOs or subDAOs of subDAOs.
                <br> <br>
Lastly, <b> there’s calibrating movement </b>. Movements that can change the direction, but are not new movements in the sense that their implementation and impact is fully known. 
While increasing an inflation rate can be compared to pressing the proverbial gas pedal, calibration movements are comparable to turning the wheel, or changing the route to the same destination. In practice, these calibration movements are member dependent budgetary allocations. Such energy allocations can be out of their “no taxation without interest materialisation” share of inflation or, through direct, un-intermediated transfers. A very interesting point here is that such in-motion energy allocations become political statements.  

